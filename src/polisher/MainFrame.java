    @Override
    public void actionPerformed(ActionEvent e) {
        int i=0;
        for (Enumeration<AbstractButton> buttons = profileGroup.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();

            if (button.isSelected()) {
                RSC.switchProfileTo(RSC.profiles.get(i));
            }
            i++;
        }
    }


