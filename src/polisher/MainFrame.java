    public void populateProfileMenu() {
        jMConfiguration.removeAll();
        for (Profile profile : RSC.profiles) {
            JRadioButtonMenuItem jRBI = new JRadioButtonMenuItem(profile.get("Name"));
            jRBI.addActionListener(this);
            jMConfiguration.add(jRBI);
            profileGroup.add(jRBI);
        }        
    }
    
    public void loadFile() {
        String fileName=RSC.guiTools.selectFileForOpen("Select file", "LaTeX file open", ".tex", "LaTeX file");
        if (fileName!=null) {
            currentFileName=fileName;
            MSV.setDocument(LaTeXDocument.fromFile(fileName));
            MSV.updateDisplay();
        }
    }
    
    public void reLoad() {
        if (currentFileName!=null) {
            MSV.setDocument(LaTeXDocument.fromFile(currentFileName));
            MSV.updateDisplay();
        }
    }
    
    public void displayPosition(int row, int column) {
        jLPosition.setText("Position: "+column+":"+row);
    }
    
    private void setDefaults() {
        profileGroup.getElements().nextElement().doClick();
    }
    
    public void showProblemText(String description) {
        jEPStatus.setText(description);
    }
    
